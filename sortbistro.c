void qqsort(int b, int e)
{
  int l = b, r = e;
  int piv = a[abs(rand()) % (e - b) + b];
  while(l <= r)
  {
    while(a[l] < piv)
      l++;
    while(a[r] > piv)
      r--;
    if(l <= r)
      swap(&a[l++], &a[r--]);
  }
  if(b < r)
    qqsort(b, r);
  if(e > l)
    qqsort(l, e);
}

